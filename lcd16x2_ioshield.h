#define PORT_CE0        10

//------------------------------------------------------------------------------
//
// I2C: I2C-1, I2C-2
//
//------------------------------------------------------------------------------
#define PORT_SDA1       3
#define PORT_SCL1       5
#define PORT_SDA2       27
#define PORT_SCL2       28

