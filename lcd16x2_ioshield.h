//
//------------------------------------------------------------------------------
#define PORT_BUTTON1	5
#define PORT_BUTTON2	6

//------------------------------------------------------------------------------
//
// LED:
//
//------------------------------------------------------------------------------
// PCB Layout
// | D1 | D2 | D3 | D4 | D7 | D6 | D5 |
//------------------------------------------------------------------------------
